                    <h3 class="font-weight-bold">Strategi Komunikasi di Era Global</h3>
                </div>
                <div class="py-4">
                    <p class="text-justify">
                        Strategi yang akan kita terapkan pada era global, dapat kita ambil dari suatu keadaan semisal saat ini kita masih dalam masa pandemi Covid 19, maka bisa dikatakan bahwa perencanaan komunikasi strategi merujuk pada kondisi krisis.
                    </p>
                    <p class="text-justify">
                        Ahli sosiologi Indonesia, Selo Soemardjan menyebut globalisasi sebagai terbentuknya sistem organisasi dan komunikasi antar masyarakat di seluruh dunia untuk mengikuti sebuah sistem dan kaidah yang sama. Yang artinya bahwa penyampaian komunikasi saat ini sudah sangat mudah menyebar ke seluruh penjuru dunia, setiap pemberitaan bisa sangat cepat menyebar, tanpa ada hambatan waktu. Proses penerimaan informasi pun sangat mudah karena saat ini penyebaran informasi melalui teknologi internet ( web, social media ), dimana orang – orang pada saat ini rata –rata sudah memiliki media penerima informasi tersebut berupa gawai / gadget yang sudah dimiliki oleh masing masing orang.
                    </p>
                    <p>
                        Maka ada beberapa hal yang bisa diperhatikan untuk kita coba terapkan dalam perencanaan strategi berkomunikasi diantaranya :
                    </p>

                    <h5 class="font-weight-bold">Penguasaan dalam menggunakan media informasi saat ini </h5>
                    <img src="../assets/img/artikel2_1.jpg" alt="" class="my-3" style="width: 35rem; height: 22rem">
                    <p class="text-justify">
                        Untuk bisa beradaptasi dalam hal penerapan komunikasi di era global / saat ini tentunya kita harus bisa menyesuaikan dulu tentunya dengan kecanggihan teknologi dimana era global benar benar telah mempermudah setiap aktivitas manusia dari sebelumnya dimana saat ini contohnya dalam berkomunikasi melalui media teknologi komunikasi  ( internet ) sangat mudah sekali proses penyampaian pesan tanpa ada hambatan jarak dan waktu. Maka seharusnya kita harus bisa dulu untuk membiasakan dan menguasai media untuk penyampaian informasi ini. Setidaknya bisa menguasai dasar – dasar penggunaan media informasi saa ini seperti contoh bahwa media perpesana online seperti Whatsapp untuk saat ini setidaknya hampir setiap orang menggunakan aplikasi ini, tidak sekedar hanya untuk mengabari seseorang namun juga menjadi media penyebaran informasi. Ada juga media social online yang banyak digunakan oleh banyak orang seperti Instagram, facebook, twitter, youtube
                        Lalu ada beberapa hal strategi dalam berkomunikasi di era krisis ini ( Pandemic covid -19, contoh penerapan langsung suatu peristiwa di era global saat ini )
                    </p>

                    <h5 class="font-weight-bold">1. Tunjukkan Empati </h5>
                    <img src="../assets/img/artikel2_2.jpg" alt="" class="my-3" style="width: 35rem; height: 22rem">
                    <p class="text-justify">
                        Mengabaikan pandemi Covid-19 sepenuhnya dan menjalankan komunikasi layaknya tidak ada apa-apa tentu bukan langkah yang tepat. Memang, di saat seperti ini, banyak orang menghabiskan waktunya secara online.
                    </p>
                    <p>
                        Oleh karena itu, tunjukkan pada khalayakmu bahwa kamu peduli terhadap mereka. tidak harus terus-terus membahas Covid-19, tapi pertimbangkan baik-baik mengenai bagaimana kamu menyampaikan pesan / konten.. Apakah dapat menimbulkan kesalahpahaman? Jangan sampai gaya komunikasi yang tidak tepat menjadi boomerang.
                    </p>

                    <h5 class="font-weight-bold">2.	Gunakan Gaya Bahasa yang Tepat </h5>
                    <img src="../assets/img/artikel2_6.jpg" alt="" class="my-3" style="width: 35rem; height: 22rem">
                    <p class="text-justify">
                        gaya bahasa yang  digunakan tentu harus sesuai dengan konteks. Misalnya, kalau biasanya gaya bahasa lebih kasual dan santai atau bahkan nyeleneh, kamu mungkin perlu mempertimbangkan untuk menggunakan gaya penyampaian yang lebih serius dalam mengkomunikasikan informasi yang berkaitan dengan Covid-19. Dan memperhatikan istilah istilah yang muncul, jangan sampai salah penyampaiaannya
                        Intinya, gunakan gaya penyampaian yang autentik, positif, dan tidak berlebihan dan dibuat-buat. Tunjukkan dukunganmu dan kamu pun menghargai dukungan mereka.  Berikan kesan bahwa mereka merupakan prioritasmu.
                    </p>

                    <h5 class="font-weight-bold">3.	Transparan & Jelas  </h5>
                    <img src="../assets/img/artikel2_3.jpg" alt="" class="my-3" style="width: 35rem; height: 22rem">
                    <p class="text-justify">
                        Di saat seperti ini, semua orang sangat peduli dengan kebersihan dan keselamatan mereka. Maka itu, sampaikan dengan jelas dan terbuka untuk menjaga keamanan mereka.
                    </p>
                    <p>
                        Misalnya, jika kamu membuka bisnis UKM yang merupakan usaha makanan, komunikasikan dengan transparan mengenai praktik keamanan dan kebersihan makanan yang kamu terapkan untuk mencegah kontaminasi dan menjaga pelangganmu tetap aman.
                    </p>
                    <p>
                        Bagaimana kamu menyiapkan makananmu? Apakah kamu menyediakan layanan pesan antar? Bagaimana kamu memastikan kebersihan produkmu selama proses pengiriman? Kalau bisa, jangan sekadar mengatakan langkah-langkah apa saja yang kamu lakukan. Tunjukkannya secara langsung dan transparan adalah cara yang bagus, misalnya dengan membagikannya di media social.
                    </p>

                    <h5 class="font-weight-bold">4.	Tetap Membuat Konten   </h5>
                    <img src="../assets/img/artikel2_4.jpg" alt="" class="my-3" style="width: 35rem; height: 22rem">
                    <p class="text-justify">
                        Berhati-hati bukan berarti berhenti berkomunikasi! Tetap terkoneksi dengan pengikutmu di media sosial dan jaga engagement dengan mereka. Karena social distancing, kebanyakan orang menghabiskan lebih banyak waktunya di dunia maya.
                    </p>
                    <p>
                        Menurut survei Kantar, penggunaan Facebook dan Instagram meningkat 40% karena dampak Covid-19. View Instagram dan Facebook Live pun bertambah dua kali lipat dalam satu minggu. Maka itu, ini kesempatanmu untuk menghasilkan konten yang unik dan relevan dengan audiens. Coba buat konten yang dapat membantu, memberi dukungan, atau menghibur bagi mereka.
                    </p>

                    <h5 class="font-weight-bold">5.	Bagikan Konten Positif    </h5>
                    <img src="../assets/img/artikel2_5.jpg" alt="" class="my-3" style="width: 35rem; height: 22rem">
                    <p class="text-justify">
                        Banyak orang yang mengaku cemas dan stres dengan informasi dan berita mengenai pandemi Covid-19 yang tak ada habisnya. Dengan begitu banyaknya konten yang “berat”, kamu bisa memberikan alternatif atau distraksi bagi audiensmu dengan menghadirkan konten yang positif, menghibur, tapi tetap peka.
                    </p>

                    <p>
                        Ingat untuk tetap hati-hati dalam menyampaikan konten agar tidak terkesan tidak sensitif.
                    </p>

